// ===== THE PASTE-BOX (adding your own entries) =====
// You collect bare words through the week, hand them to Claude, and Claude
// gives you ONE block in the plain-lines format below. You paste that block
// here, tap Save, and it lands in the added-entries drawer.
//
// The format Claude hands you looks like this (one or more entries,
// each separated by a line containing only ---):
//
//   type: word
//   term: perfunctory
//   meaning: done with minimal effort, just to get it over with
//   replaces: half-hearted, going through the motions
//   example: He gave a perfunctory nod and moved on.
//   register: work-safe; literary
//   ---
//   type: phrase
//   term: ...
//
// All six fields are required. If anything's wrong, NOTHING saves and a
// plain-English message tells you exactly what to fix.

// The six fields every entry must have.
const REQUIRED_FIELDS = ["type", "term", "meaning", "replaces", "example", "register"];

// Turn the pasted text into a list of entry-objects, OR a list of problems.
// Returns { ok: true, parsed: [...] } if everything's clean,
// or { ok: false, errors: [...] } if anything's wrong. Saves NOTHING itself —
// that's the caller's job, and only if ok is true (all-or-nothing).
function parseBatch(text) {
  const errors = [];

  // Split the whole paste into blocks on a line that is just "---".
  const blocks = text
    .split(/^\s*---\s*$/m)        // break on a --- line
    .map(b => b.trim())           // tidy each block
    .filter(b => b.length > 0);   // drop empty blocks (e.g. trailing ---)

  if (blocks.length === 0) {
    return { ok: false, errors: ["The box is empty — nothing to add."] };
  }

  const parsed = [];

  // Walk each block and turn its "field: value" lines into an object.
  blocks.forEach((block, i) => {
    const entryNumber = i + 1;
    const fields = {};

    // Each line is "field: value". Split on the FIRST colon only, so colons
    // inside the value (like in an example sentence) are left alone.
    const lines = block.split("\n");
    for (const line of lines) {
      const trimmed = line.trim();
      if (trimmed === "") continue; // skip blank lines inside a block

      const colon = trimmed.indexOf(":");
      if (colon === -1) continue;   // a line with no colon — ignore it

      const key = trimmed.slice(0, colon).trim().toLowerCase();
      const value = trimmed.slice(colon + 1).trim();
      fields[key] = value;
    }

    // Name the entry for error messages — use its term if we got one.
    const label = fields.term ? '"' + fields.term + '"' : "(no term)";

    // Check every required field is present and non-empty.
    for (const field of REQUIRED_FIELDS) {
      if (!fields[field]) {
        errors.push(
          "Entry " + entryNumber + " " + label +
          " is missing its " + field + " line."
        );
      }
    }

    // Check type is exactly "word" or "phrase".
    if (fields.type && fields.type !== "word" && fields.type !== "phrase") {
      errors.push(
        "Entry " + entryNumber + " " + label +
        ' has type "' + fields.type + '" — it must be word or phrase.'
      );
    }

    parsed.push(fields);
  });

  if (errors.length > 0) {
    return { ok: false, errors: errors };
  }

  return { ok: true, parsed: parsed };
}

// Work out the next free id for a given type ("word" → w.., "phrase" → p..).
// Defensive: instead of "highest + 1", we find the first number NOT already
// used across seed + additions. This survives gaps and odd manual edits —
// it can never hand out an id that already exists.
function nextFreeId(type) {
  const prefix = type === "word" ? "w" : "p";

  // Collect every number already used with this prefix, across everything.
  const used = new Set();
  for (const entry of entries) {
    if (entry.id && entry.id[0] === prefix) {
      const num = parseInt(entry.id.slice(1), 10);
      if (!isNaN(num)) used.add(num);
    }
  }

  // Count up from 1 until we hit a number nobody's using.
  let n = 1;
  while (used.has(n)) n++;

  // Format as a 2-digit id (w01, p07). Numbers past 99 just get longer (w100).
  const padded = n < 10 ? "0" + n : "" + n;
  return prefix + padded;
}

// Check whether a term already exists (in seed or additions), case-insensitive.
// Used for the "warn but allow" duplicate notice.
function termAlreadyExists(term) {
  const lower = term.toLowerCase();
  return entries.some(entry => entry.term.toLowerCase() === lower);
}

// Build the inner markup for the status banner. `kind` is "ok" or "err".
// `title` is the bold first line; `sub` (optional) is the softer second line
// (may contain <strong> for an offending field name). The caller also sets
// the matching class on #paste-status so the colours + icon land.
function statusBanner(kind, title, sub) {
  // The two icons: circle-check for success, alert-circle for error.
  const okIcon =
    "<svg viewBox='0 0 24 24' fill='none' stroke='currentColor' stroke-width='2.5' stroke-linecap='round' stroke-linejoin='round'>" +
      "<path d='M22 11.08V12a10 10 0 1 1-5.93-9.14'></path>" +
      "<polyline points='22 4 12 14.01 9 11.01'></polyline>" +
    "</svg>";
  const errIcon =
    "<svg viewBox='0 0 24 24' fill='none' stroke='currentColor' stroke-width='2.5' stroke-linecap='round' stroke-linejoin='round'>" +
      "<circle cx='12' cy='12' r='10'></circle>" +
      "<line x1='12' y1='8' x2='12' y2='12'></line>" +
      "<line x1='12' y1='16' x2='12.01' y2='16'></line>" +
    "</svg>";

  const icon = kind === "ok" ? okIcon : errIcon;
  const subLine = sub ? "<span class='status-sub'>" + sub + "</span>" : "";

  return (
    "<span class='status-icon'>" + icon + "</span>" +
    "<span class='status-text'>" +
      "<span class='status-title'>" + title + "</span>" +
      subLine +
    "</span>"
  );
}

// The Save button's whole job, start to finish.
function saveBatch() {
  const box = document.getElementById("paste-box");
  const status = document.getElementById("paste-status");
  const text = box.value;

  // Step 1: parse + validate. Nothing is saved yet.
  const result = parseBatch(text);

  // Step 2: if anything's wrong, show every problem and STOP. Box keeps text.
  if (!result.ok) {
    status.className = "paste-status is-err";
    status.innerHTML = statusBanner(
      "err",
      "Nothing saved.",
      result.errors.join("<br>") + "<br>Fix it and save again — your text is still here."
    );
    return;
  }

  // Step 3: all clean. Assign a fresh id to each, note any duplicate terms.
  // We add to the in-memory `entries` as we go, so each new id accounts for
  // the ones assigned moments earlier in the same batch.
  const added = loadAdded();
  const duplicateNotes = [];

  for (const fields of result.parsed) {
    if (termAlreadyExists(fields.term)) {
      duplicateNotes.push(fields.term);
    }

    const entry = {
      id: nextFreeId(fields.type),   // app assigns the id — ignores any pasted id
      type: fields.type,
      term: fields.term,
      meaning: fields.meaning,
      replaces: fields.replaces,
      example: fields.example,
      register: fields.register
    };

    added.push(entry);
    entries.push(entry);  // so nextFreeId + duplicate check see it immediately
  }

  // Step 4: save the added drawer, then refresh everything on screen.
  saveAdded(added);
  rebuildEntries();   // rebuild the combined list cleanly from storage
  box.value = "";     // clear the box — it worked

  // Build the success message: a title line, plus an optional duplicate sub-line.
  const title = "Saved " + result.parsed.length +
    (result.parsed.length === 1 ? " entry to your list." : " entries to your list.");

  let sub = "";
  if (duplicateNotes.length > 0) {
    sub = "Heads up — already in your list: " +
      duplicateNotes.join(", ") + " (added anyway).";
  }

  status.className = "paste-status is-ok";
  status.innerHTML = statusBanner("ok", title, sub);

  // Refresh the screens that depend on the entry list.
  // Clear any search text first, so newly-added entries aren't hidden by a
  // leftover filter, then re-render the full list (and refresh the count).
  const searchBox = document.getElementById("all-search");
  if (searchBox) searchBox.value = "";
  showAllEntries();
  showToday();
  showRetrieve();
}

// ===== DELETE AN ADDED ENTRY =====
// Delete is allowed ONLY for entries you added yourself (the sharper-added
// drawer). Seed entries are baked into the code — "deleting" one would just
// reload on refresh — so the trash diamond never appears for them.
//
// Flow: openEntry/showToday record the on-screen entry in `currentEntry` and
// call setDeleteAffordance, which shows the trash diamond only for added
// entries. Tapping it (askDelete) reveals the inline confirm strip; Cancel
// hides it again; Delete (confirmDelete) removes the entry from the added
// drawer AND its tracking record, then returns to a refreshed All Entries.

// Which entry is currently shown on a detail card. Set on every fill path.
let currentEntry = null;

// Is this entry one of YOUR added ones (vs a baked-in seed entry)?
function isAddedEntry(id) {
  return loadAdded().some(e => e.id === id);
}

// Show/hide the trash diamond for a freshly-opened detail card, and ALWAYS
// hide the confirm strip (so a half-open confirm from a previous view can
// never linger). `screen` is "word" or "phrase" — picks which card's bits.
function setDeleteAffordance(entry, screen) {
  const btn = document.getElementById(screen + "-delete");
  const confirm = document.getElementById(screen + "-delete-confirm");

  // Confirm strip starts closed every time.
  if (confirm) confirm.classList.add("is-hidden");

  // Trash diamond appears only for added entries.
  if (btn) {
    if (entry && isAddedEntry(entry.id)) {
      btn.classList.remove("is-hidden");
    } else {
      btn.classList.add("is-hidden");
    }
  }
}

// Hide BOTH detail screens' trash diamonds + confirm strips outright.
// Called when leaving for a non-deletable view (e.g. tapping a Home tile,
// which always shows today's seed pick).
function hideDeleteAffordance() {
  ["word", "phrase"].forEach(screen => {
    const btn = document.getElementById(screen + "-delete");
    const confirm = document.getElementById(screen + "-delete-confirm");
    if (btn) btn.classList.add("is-hidden");
    if (confirm) confirm.classList.add("is-hidden");
  });
}

// Trash tapped: hide the trash, reveal the confirm strip for whichever
// detail screen we're on (decided by currentEntry's type).
function askDelete() {
  if (!currentEntry) return;
  const screen = currentEntry.type === "word" ? "word" : "phrase";
  document.getElementById(screen + "-delete").classList.add("is-hidden");
  document.getElementById(screen + "-delete-confirm").classList.remove("is-hidden");
}

// Cancel: put it back the way it was — confirm hidden, trash showing.
function cancelDelete() {
  if (!currentEntry) return;
  const screen = currentEntry.type === "word" ? "word" : "phrase";
  document.getElementById(screen + "-delete-confirm").classList.add("is-hidden");
  document.getElementById(screen + "-delete").classList.remove("is-hidden");
}

// Confirmed delete: remove the entry from the added drawer by id, remove its
// tracking record too (no orphan), rebuild the combined list, then refresh
// the dependent screens and land back on a clean All Entries.
function confirmDelete() {
  if (!currentEntry) return;
  const id = currentEntry.id;

  // 1. Drop it from the added-entries drawer.
  const added = loadAdded().filter(e => e.id !== id);
  saveAdded(added);

  // 2. Drop its tracking record, if any (so nothing dangles in sharper-tracking).
  const tracking = loadTracking();
  if (tracking[id]) {
    delete tracking[id];
    saveTracking(tracking);
  }

  // 3. Rebuild the combined list so the entry is gone everywhere downstream.
  rebuildEntries();
  currentEntry = null;

  // 4. Refresh the screens that read the entry list, then go to All Entries.
  //    Clear any leftover search text so the refreshed list shows in full.
  const searchBox = document.getElementById("all-search");
  if (searchBox) searchBox.value = "";
  showAllEntries();
  showToday();
  showRetrieve();
  backTarget = "screen-home";   // All Entries is top-level; its back goes Home
  showScreen("screen-all");
}

// ===== OPEN A SINGLE ENTRY FROM THE LIST =====
// Called when you tap a row in All Entries. Finds the entry by its id,
// fills the matching detail card (word card for words, phrase card for
// phrases), sets the header label to the plain "Word"/"Phrase" form
// (NOT "of the day" — that wording is only for today's home pick),
// remembers All Entries as the back target so the back arrow returns you
// to the list, then shows that screen.
function openEntry(id) {
  const entry = entries.find(e => e.id === id);
  if (!entry) return;

  // Remember which entry is on screen, so the delete handlers know what to
  // act on. Set on EVERY detail-fill path (here + showToday).
  currentEntry = entry;

  // Opened from the list, so the back arrow should return to the list.
  backTarget = "screen-all";

  if (entry.type === "word") {
    document.getElementById("word-term").textContent = entry.term;
    document.getElementById("word-register").textContent = entry.register;
    document.getElementById("word-meaning").textContent = entry.meaning;
    document.getElementById("word-replaces").textContent = entry.replaces;
    document.getElementById("word-example").textContent = entry.example;
    // Plain label, because this isn't today's pick — it's a library lookup.
    document.querySelector("#screen-word .detail-label").textContent = "Word";
    // Show the trash diamond only if this is one of YOUR added entries.
    setDeleteAffordance(entry, "word");
    showScreen("screen-word");
  } else {
    document.getElementById("phrase-term").textContent = entry.term;
    document.getElementById("phrase-register").textContent = entry.register;
    document.getElementById("phrase-meaning").textContent = entry.meaning;
    document.getElementById("phrase-replaces").textContent = entry.replaces;
    document.getElementById("phrase-example").textContent = entry.example;
    // Plain label, same reasoning.
    document.getElementById("phrase-label").textContent = "Phrase";
    // Show the trash diamond only if this is one of YOUR added entries.
    setDeleteAffordance(entry, "phrase");
    showScreen("screen-phrase");
  }
}

// ===== THE ALL ENTRIES LIST =====
// Fills #all-list with one row per entry: the term in serif + a small
// coloured diamond dot (plum = word, amber = phrase). Tapping a row opens
// that entry's detail card. An optional filter string narrows the list live
// as you type in the search box.
function showAllEntries(filter) {
  const list = document.getElementById("all-list");
  const countEl = document.getElementById("all-count");

  // Normalise the search text (lowercase, trimmed). Empty = show everything.
  const needle = (filter || "").trim().toLowerCase();

  // Which entries match? If there's no search text, that's all of them.
  const shown = needle
    ? entries.filter(e => e.term.toLowerCase().includes(needle))
    : entries;

  // The count on the right of the header always reflects the FULL library.
  if (countEl) countEl.textContent = entries.length;

  // Nothing in the whole app yet.
  if (entries.length === 0) {
    list.innerHTML = "<p class='all-empty'>No entries yet.</p>";
    return;
  }

  // Search typed, but nothing matched it.
  if (shown.length === 0) {
    list.innerHTML = "<p class='all-empty'>No matches.</p>";
    return;
  }

  // Build one row per matching entry.
  let html = "";
  for (const entry of shown) {
    const dotClass = entry.type === "word" ? "entry-dot-word" : "entry-dot-phrase";
    html +=
      "<button class='entry-row' onclick=\"openEntry('" + entry.id + "')\">" +
        "<span class='entry-term'>" + entry.term + "</span>" +
        "<span class='entry-dot " + dotClass + "'></span>" +
      "</button>";
  }
  list.innerHTML = html;
}

