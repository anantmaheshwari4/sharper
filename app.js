// ===== ADDED-ENTRIES STORE =====
// This is the SECOND localStorage drawer. The first (sharper-tracking, below)
// holds how you're doing on each word. THIS one holds the CONTENT of words
// YOU add via the paste-box — the words themselves. Two drawers, two jobs.
//
// Shape: an array of entries, same shape as `seed` entries:
//   [ { id: "w11", type: "word", term: "...", meaning: "...", ... }, ... ]
// Empty array if you've never added anything.

const ADDED_KEY = "sharper-added";

// Read your added entries out of localStorage.
function loadAdded() {
  const raw = localStorage.getItem(ADDED_KEY);
  if (!raw) return [];
  try {
    return JSON.parse(raw);
  } catch (e) {
    // If the saved data is somehow corrupted, start clean rather than crash.
    return [];
  }
}

// Write your added entries back into localStorage.
function saveAdded(added) {
  localStorage.setItem(ADDED_KEY, JSON.stringify(added));
}


// ===== THE COMBINED LIST (seed + your additions) =====
// Everything downstream — the picker, the retrieval scheduler, the All
// Entries list — reads from THIS, not from `seed` directly. It's built
// fresh from seed + whatever's in the added drawer. Rebuilt whenever you
// add a batch, so new words join rotation immediately.

let entries = [];

function rebuildEntries() {
  entries = seed.concat(loadAdded());
}


// ===== RETRIEVAL TRACKING STORE =====
// Each entry's CONTENT lives in `entries` (seed + additions).
// Each entry's TRACKING (when to test it next, etc.) lives here, in the
// browser's localStorage, keyed by the entry's id. They get stitched
// together at runtime. We keep them separate because code can't write to
// itself — but localStorage can be written to every time you tap hit/miss.

const TRACKING_KEY = "sharper-tracking";

// Read the whole tracking object out of localStorage.
// Shape: { "w01": { nextDue: 904, gap: 14, misses: 2 }, ... }
// If nothing's saved yet (first ever load), return an empty object.
function loadTracking() {
  const raw = localStorage.getItem(TRACKING_KEY);
  if (!raw) return {};
  try {
    return JSON.parse(raw);
  } catch (e) {
    // If the saved data is somehow corrupted, start clean rather than crash.
    return {};
  }
}

// Write the whole tracking object back into localStorage.
function saveTracking(tracking) {
  localStorage.setItem(TRACKING_KEY, JSON.stringify(tracking));
}


// ===== RETRIEVAL SCHEDULER (the retrieval brain) =====
// All the dials live here, named, so they're easy to change later.

const RETRIEVAL_SETTINGS = {
  firstPassGap: 2,    // a brand-new word's first test comes ~2 days after first seen
  missGap: 2,         // a missed word resets all the way back to ~2 days (full reset)
  hitMultiplier: 2,   // a hit multiplies the current gap (gap grows: 2 → 4 → 8 → 16...)
  dailyCount: 4       // up to 4 retrieval tests served per morning
};

// When you first SEE an entry (it shows up on the Word/Phrase screen),
// it gets entered into the tracking cabinet and scheduled for its first
// retrieval test ~2 days out. If it's already being tracked, this does
// nothing (we never re-schedule something already in the system).
function ensureScheduled(id, today) {
  const tracking = loadTracking();
  if (tracking[id]) return; // already known — leave it alone

  tracking[id] = {
    nextDue: today + RETRIEVAL_SETTINGS.firstPassGap,
    gap: RETRIEVAL_SETTINGS.firstPassGap,
    misses: 0
  };
  saveTracking(tracking);
}

// Which entries are due for retrieval today?
// An entry is "due" if it's being tracked AND its nextDue day has arrived
// (nextDue <= today). Returns the full list of due entries (could be more
// than 4 — the screen layer trims to 4 and the rest naturally stay due
// tomorrow, i.e. they queue).
function getDueEntries(today) {
  const tracking = loadTracking();
  const due = [];

  for (const entry of entries) {
    const record = tracking[entry.id];
    if (record && record.nextDue <= today) {
      due.push(entry);
    }
  }

  return due;
}

// You MISSED a word. Full reset: gap goes back to ~2 days, nextDue is
// ~2 days out, and the miss counter ticks up by one.
function markMiss(id, today) {
  const tracking = loadTracking();
  const record = tracking[id];
  if (!record) return;

  record.gap = RETRIEVAL_SETTINGS.missGap;
  record.nextDue = today + RETRIEVAL_SETTINGS.missGap;
  record.misses = record.misses + 1;
  saveTracking(tracking);
}

// You HIT a word. Stretch: the gap multiplies (2 → 4 → 8...), and the
// next test is pushed that many days out. The word never leaves rotation —
// it just comes back less often as it sticks.
function markHit(id, today) {
  const tracking = loadTracking();
  const record = tracking[id];
  if (!record) return;

  record.gap = record.gap * RETRIEVAL_SETTINGS.hitMultiplier;
  record.nextDue = today + record.gap;
  saveTracking(tracking);
}


// ===== THE DAILY PICKER (the brain) =====

// How many days back we refuse to repeat an entry.
const NO_REPEAT_DAYS = 10;

// A stable "day number" — counts days since Jan 1, 2024.
// Same all day, ticks up by 1 at midnight. This is what makes the pick rotate daily.
function getDayNumber() {
  const start = new Date(2024, 0, 1);
  const now = new Date();
  const msPerDay = 1000 * 60 * 60 * 24;
  return Math.floor((now - start) / msPerDay);
}

// Pick one entry of a given type ("word" or "phrase") for a given day,
// avoiding anything picked in the last NO_REPEAT_DAYS days.
function pickForDay(type, dayNumber) {
  const pool = entries.filter(entry => entry.type === type);
  if (pool.length === 0) return null;

  // Figure out which pool-indexes were used in the recent window.
  const recentlyUsed = [];
  const windowSize = Math.min(NO_REPEAT_DAYS, pool.length - 1);
  for (let back = 1; back <= windowSize; back++) {
    recentlyUsed.push((dayNumber - back) % pool.length);
  }

  // Today's raw pick.
  let index = dayNumber % pool.length;

  // If today's pick was used recently, step forward until we find a free one.
  let safety = 0;
  while (recentlyUsed.includes(index) && safety < pool.length) {
    index = (index + 1) % pool.length;
    safety++;
  }

  return pool[index];
}


